'''

Write the necessary code to display the area and perimeter of a rectangle that has a width of 2.4 and a height of 6.4.

'''

height = 6.4
width = 2.4

rectangle_area = width * height

rectangle_perimeter = 2*(width + height)

print("The area of the rectangle is: " + str(rectangle_area) + " and its perimeter is: " + str(rectangle_perimeter) + ".")
